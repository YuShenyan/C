#include <stdio.h>
#include <math.h>

int main()
{
	double a, x, x1 = 1;
	scanf("%lf", &a);
	do
	{
		x1 = x;
		x = (x + a / x) / 2;
	} while (fabs(x - x1) >= 1E-5);
	printf("%.5lf", x);
	return 0;
}
