#include <stdio.h>
#include <math.h>

int main() {
	float n, i, sum = 0;
	scanf("%f", &n);
	for (i = 1; i <= n; i++) {
		sum += pow(-1, i - 1) * i / (2 * i - 1);
		//	printf("|%.3f|", sum);
	}
	printf("%.3f", sum);
	return 0;
}
