#include <stdio.h>

int main() {
	int n, i;
	scanf("%d", &n);
	float a[100], sum = 0;
	for (i = 0; i < n; i++) {
		scanf("%f", &a[i]);
		sum += a[i];
	}
	printf("%.2f", sum / n);
	return 0;
}
