#include <stdio.h>

int main() {
	int i;
	float jin = 0;
	scanf("%d", &i);
	if (i > 1000000) {
		jin += (i - 1000000) * 0.01;
		i = 1000000;
	}
	if (i > 600000) {
		jin += (i - 600000) * 0.015;
		i = 600000;
	}
	if (i > 400000) {
		jin += (i - 400000) * 0.03;
		i = 400000;
	}
	if (i > 200000) {
		jin += (i - 200000) * 0.05;
		i = 200000;
	}
	if (i > 100000) {
		jin += (i - 100000) * 0.075;
		i = 100000;
	}
	jin += i * 0.1;
	printf("%.2f", jin);
	return 0;
}
