#include <stdio.h>

int main() {
	int i, ge = 0;
	for (i = 100; i < 500; i++) {
		if ((i % 7 == 0 || i % 11 == 0 ) && !(i % 7 == 0 && i % 11 == 0))
			ge++;
	}
	printf("%d", ge);
	return 0;
}
