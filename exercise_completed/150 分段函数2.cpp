#include <stdio.h>

int main() {
	int x;
	scanf("%d", &x);
	if (x < 1) {
		printf("%d", x);
		return 0;
	}
	if (x >= 1 && x < 10) {
		printf("%d", 2 * x - 1);
		return 0;
	}
	if (x >= 10) {
		printf("%d", 3 * x - 11);
		return 0;
	}
}
