#include <stdio.h>

int main() {
	int i, n, cheng = 1, he = 0;
	scanf("%d", &n);
	for (i = 1; i <= n; i++) {
		cheng *= i;
		he += cheng;
	}
	printf("%d %d %d", n, cheng, he);
	return 0;
}
