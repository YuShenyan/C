#include <stdio.h>

int main() {
	int a[160], p, i, j = 0, k, sum = 0, n = 0;
	scanf("%d", &p);
	i = 2;
	while ( j < p + 10 ) {
		int m = 0;
		for (k = 2; k < i; k++) {
			if (i % k == 0) {
				m = 1;
			}
		}
		if (m == 0) {
			a[j] = i;
			j++;
		}
		i++;
	}
	for (j = p - 1; j < p + 10; j++) {
		sum += a[j];
	}
	printf("%d", sum);
	return 0;
}
