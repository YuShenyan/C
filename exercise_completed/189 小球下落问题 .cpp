#include <stdio.h>

int main() {
	float s = 0, h = 100;
	int n, i;
	scanf("%d", &n);
	for (i = 0; i < n; i++) {
		h /= 2;
		s += 3 * h;
	}
	s -= h;
	printf("s=%.3f h=%.3f", s, h);
	return 0;
}
