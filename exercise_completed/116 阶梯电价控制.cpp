#include <stdio.h>

int main() {
	float a, fee;
	scanf("%f", &a);
	if (a <= 110)
		fee = a * 0.5;
	else if (110 < a && a <= 210) {
		fee = 55 + 0.55 * (a - 110);
	} else {
		fee = 110 + (a - 210) * 0.7;
	}
	printf("%.2f", fee);
	return 0;
}
