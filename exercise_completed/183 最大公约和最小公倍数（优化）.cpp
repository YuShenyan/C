#include <stdio.h>

int main() {
	int m, n, temp, a, b;
	scanf("%d %d", &m, &n);
	a = (m > n  ) ? m : n;
	b = (m > n  ) ? n : m;
	while (a % b != 0) {
		temp = a % b;
		a = b;
		b = temp;
	}
	printf("%d %d", b, m * n / b);
	return 0;
}
