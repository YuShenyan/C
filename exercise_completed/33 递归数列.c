#include <stdio.h>

double function(int n) {
	int i;
	double a = 1;
	for (i = 1; i < n; i++) {
		a = 1 / (1 + a);
	}
	return a;
}

int main() {
	int n;
	scanf("%d", &n);
	printf("%.6lf", function(n));
	return 0;
}
