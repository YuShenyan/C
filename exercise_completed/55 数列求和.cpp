#include <stdio.h>

int main() {
	int n, i;
	float a[30], b[30], sum = 3.5;
	scanf("%d", &n);
	a[0] = 2;
	a[1] = 3;
	b[0] = 1;
	b[1] = 2;
	for (i = 2; i < n; i++) {
		a[i] = a[i - 1] + a[i - 2];
		b[i] = b[i - 1] + b[i - 2];
		sum += a[i] / b[i];
	}
	printf("%.2f", sum);
	return 0;
}
